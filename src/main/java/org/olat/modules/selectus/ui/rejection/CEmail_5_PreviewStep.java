package org.olat.modules.selectus.ui.rejection;

import org.olat.core.gui.UserRequest;
import org.olat.core.gui.components.form.flexible.impl.Form;
import org.olat.core.gui.control.WindowControl;
import org.olat.core.gui.control.generic.wizard.BasicStep;
import org.olat.core.gui.control.generic.wizard.PrevNextFinishConfig;
import org.olat.core.gui.control.generic.wizard.Step;
import org.olat.core.gui.control.generic.wizard.StepFormController;
import org.olat.core.gui.control.generic.wizard.StepsRunContext;

import org.olat.modules.selectus.model.mail.EmailVariables;

/**
 * 
 * Initial date: 22.09.2014<br>
 * @author srosse, stephane.rosse@frentix.com, http://www.frentix.com
 *
 */
public class CEmail_5_PreviewStep extends BasicStep {
	
	private final EmailVariables emailVar;
	
	public CEmail_5_PreviewStep(UserRequest ureq, EmailVariables emailVar) {
		super(ureq);
		this.emailVar = emailVar;
		setI18nTitleAndDescr("wizard.mail.preview.title", "wizard.mail.preview.title");
		setNextStep(Step.NOSTEP);
	}

	@Override
	public PrevNextFinishConfig getInitialPrevNextFinishConfig() {
		return new PrevNextFinishConfig(true, false, true);
	}

	@Override
	public StepFormController getStepController(UserRequest ureq, WindowControl wControl, StepsRunContext runContext, Form form) {
		emailVar.setShowAttachmentWarning(false);
		return new PreviewEmailsController(ureq, wControl, runContext, form, emailVar);
	}
}
