package org.olat.commons.calendar.restapi;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlRootElement;

import org.olat.commons.calendar.model.KalendarEventLink;

/**
 * 
 * Initial date: 25 f√©vr. 2022<br>
 * @author srosse, stephane.rosse@frentix.com, http://www.frentix.com
 *
 */
@XmlAccessorType(XmlAccessType.FIELD)
@XmlRootElement(name = "eventLinkVO")
public class EventLinkVO {
	
	private String id;
	private String provider;
	private String displayName;
	private String uri;
	private String iconCssClass;
	
	public EventLinkVO() {
		//
	}
	
	public EventLinkVO(KalendarEventLink event) {
		id = event.getId();
		provider = event.getProvider();
		displayName = event.getDisplayName();
		uri = event.getURI();
		iconCssClass = event.getIconCssClass();
	}

	public String getId() {
		return id;
	}
	
	public void setId(String id) {
		this.id = id;
	}

	public String getProvider() {
		return provider;
	}

	public void setProvider(String provider) {
		this.provider = provider;
	}

	public String getDisplayName() {
		return displayName;
	}

	public void setDisplayName(String displayName) {
		this.displayName = displayName;
	}

	public String getUri() {
		return uri;
	}

	public void setUri(String uri) {
		this.uri = uri;
	}

	public String getIconCssClass() {
		return iconCssClass;
	}

	public void setIconCssClass(String iconCssClass) {
		this.iconCssClass = iconCssClass;
	}
}
